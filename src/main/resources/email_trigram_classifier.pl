classify_email(Text, Result, SpamPercent, HamPercent) :-
    split_string(Text, " ", "", Words),
    findall((W1, W2, W3), (nth0(I, Words, W1), nth0(J, Words, W2), nth0(K, Words, W3), J is I + 1, K is J + 1, spam_trigram(W1, W2, W3)), SpamTrigrams),
    findall((W1, W2, W3), (nth0(I, Words, W1), nth0(J, Words, W2), nth0(K, Words, W3), J is I + 1, K is J + 1, ham_trigram(W1, W2, W3)), HamTrigrams),
    length(SpamTrigrams, SpamCount),
    length(HamTrigrams, HamCount),
    Total is SpamCount + HamCount,
    (Total > 0 ->
            SpamPercent is (SpamCount / Total) * 100,
            HamPercent is (HamCount / Total) * 100;
            SpamPercent = 0.0,
            HamPercent = 0.0
    ),
    (SpamCount > HamCount -> Result = "spam"; Result = "ham").